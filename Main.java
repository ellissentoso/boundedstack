package stack;


public class Main {

    public static void main(String[] args) {
        // Browser history
        System.out.println("BROWSER");
        BoundedStack<String> browser = new BoundedStack<>(50);
        for (int i = 0; i < 51; i++) {
            browser.push("192.168.1." + i);
        }
        while (!browser.isEmpty()) {
            System.out.println(browser.pop());
        }

        // Maji games
        System.out.println();
        System.out.println("MAJI");
        BoundedStack<Double> maji = new BoundedStack<>(50);
        for (int i = 0; i < 51; i++) {
            maji.push(i / 0.9);
        }
        while (!maji.isEmpty()) {
            System.out.println(maji.pop());
        }
    }
}
